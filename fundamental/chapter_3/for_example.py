#!/usr/bin/python3
# -*- coding: utf-8 -*-
# File Name : for_example.py


def main():
    """
        Bab Statement Kontrol
        Bagian Statement Perulangan
        Statement for
    """

    # menggunakan for untuk rentang nilai tertentu
    for nRead in range(1, 6):
        print("%d: Hello World!" % nRead)


if __name__ == "__main__":
    main()
