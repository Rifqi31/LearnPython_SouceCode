# File Name : for_example_2.py


def main():
    """
        Bab Statement Kontol
        Bagian Statement Perulangan
        Statement for
    """

    # menggunakan for pada tipe list
    for matkul in ['Algoritma Dasar', 'Struktur Data', 'Perancangan Sistem']:
        print(matkul)


if __name__ == "__main__":
    main()
