#!/usr/bin/python3
# -*- coding: utf-8 -*-
# File Name : while_example_1.py


def main():
    """
        Bab Statement Kontrol
        Bagian Struktur Pengulangan
        Statement while
    """

    # menentukan banyaknya pengulangan
    n = 5

    # melakukan pengulangan
    i = 1
    while i <= n:
        print(i)
        i += 1


if __name__ == "__main__":
    main()
