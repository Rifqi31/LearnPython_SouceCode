#!/usr/bin/python3
# -*- coding: utf-8 -*-
# File Name : while_example_3.py


def main():
    """
        Bab Statement Kontrol
        Bagian Struktur Pengulangan
        Statement While
    """

    # melakukan perulangan
    nCounter = 0
    while nCounter < 10:
        print("Baris ke-%d Hello World" % nCounter)
        nCounter += 1


if __name__ == "__main__":
    main()
