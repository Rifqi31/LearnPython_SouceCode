#!/usr/bin/python3
# -*- coding: utf-8 -*-
# File Name : while_example_5.py


def main():
    """
        Bab Statement Kontrol
        Bagian Struktur Perulangan
        Statement while
    """

    # melakukan perulangan
    nCounter = 6
    while nCounter >= 1:
        print("%d : Hello World!" % nCounter)
        nCounter -= 1


if __name__ == "__main__":
    main()
